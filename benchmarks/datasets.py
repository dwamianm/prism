"""Benchmark dataset loaders for PRME evaluation.

Provides unified loaders for external benchmark datasets. Each loader
tries to read real downloaded data first, then falls back to synthetic
data generated by the existing benchmark modules.

External datasets can be obtained by running::

    python scripts/download_benchmarks.py

Datasets are expected at:

- **LoCoMo**: ``data/benchmarks/locomo/locomo10.json``
  Paper: https://arxiv.org/abs/2402.17753
  Repo:  https://github.com/snap-research/locomo

- **LongMemEval**: ``data/benchmarks/longmemeval/longmemeval.json``
  Paper: https://arxiv.org/abs/2410.10813
  Repo:  https://github.com/xiaowu0162/LongMemEval

If the real data files are not present, the loaders return synthetic
examples generated in-process (no network access required).
"""

from __future__ import annotations

import json
import logging
from pathlib import Path

logger = logging.getLogger(__name__)

_PROJECT_ROOT = Path(__file__).resolve().parent.parent
_DEFAULT_LOCOMO_PATH = _PROJECT_ROOT / "data" / "benchmarks" / "locomo"
_DEFAULT_LONGMEMEVAL_PATH = _PROJECT_ROOT / "data" / "benchmarks" / "longmemeval"


def load_locomo_dataset(path: str | None = None) -> list[dict]:
    """Load the LoCoMo benchmark dataset.

    Tries to load real data from *path* (or the default location). If the
    data is not available, falls back to generating a synthetic conversation
    using :func:`benchmarks.locomo.generate_conversation`.

    Args:
        path: Directory containing LoCoMo JSON files. Defaults to
            ``data/benchmarks/locomo/`` relative to the project root.

    Returns:
        List of conversation dicts. Each dict contains ``turns`` (list of
        role/content pairs) and ``questions`` (list of QA pairs with
        ``question``, ``answer``, and ``category`` keys).
    """
    data_dir = Path(path) if path else _DEFAULT_LOCOMO_PATH

    # Try real dataset first
    for candidate in ["locomo10.json", "locomo_stub.json"]:
        filepath = data_dir / candidate
        if filepath.exists():
            logger.info("Loading LoCoMo data from %s", filepath)
            with open(filepath) as f:
                data = json.load(f)
            logger.info("Loaded %d LoCoMo conversations", len(data))
            return data

    # Fall back to synthetic generation
    logger.info("No LoCoMo data found at %s; generating synthetic dataset", data_dir)
    from benchmarks.locomo import generate_conversation

    turns, queries = generate_conversation(300)
    return [
        {
            "conversation_id": "synthetic-001",
            "turns": [
                {"role": t.role, "content": t.content, "day": t.day}
                for t in turns
            ],
            "questions": [
                {
                    "question": q.query,
                    "answer": ", ".join(q.expected_keywords),
                    "category": q.category,
                }
                for q in queries
            ],
        }
    ]


def load_longmemeval_dataset(path: str | None = None) -> list[dict]:
    """Load the LongMemEval benchmark dataset.

    Tries to load real data from *path* (or the default location). If the
    data is not available, falls back to generating synthetic test cases
    from the built-in LongMemEval generators.

    The LongMemEval benchmark (arXiv 2410.10813) tests five core abilities:
    information extraction, multi-session reasoning, temporal reasoning,
    knowledge updates, and abstention.

    Args:
        path: Directory containing LongMemEval JSON files. Defaults to
            ``data/benchmarks/longmemeval/`` relative to the project root.

    Returns:
        List of test case dicts. Each dict contains ``id``, ``ability``,
        ``sessions`` (list of session dicts with messages), ``question``,
        and ``answer``.
    """
    data_dir = Path(path) if path else _DEFAULT_LONGMEMEVAL_PATH

    # Try real dataset first
    for candidate in ["longmemeval.json", "longmemeval_stub.json"]:
        filepath = data_dir / candidate
        if filepath.exists():
            logger.info("Loading LongMemEval data from %s", filepath)
            with open(filepath) as f:
                data = json.load(f)
            logger.info("Loaded %d LongMemEval test cases", len(data))
            return data

    # Fall back to synthetic generation
    logger.info(
        "No LongMemEval data found at %s; generating synthetic dataset", data_dir
    )
    from benchmarks.longmemeval import (
        _generate_info_extraction_cases,
        _generate_multi_session_cases,
        _generate_temporal_cases,
        _generate_knowledge_update_cases,
        _generate_abstention_cases,
    )

    generators = [
        _generate_info_extraction_cases,
        _generate_multi_session_cases,
        _generate_temporal_cases,
        _generate_knowledge_update_cases,
        _generate_abstention_cases,
    ]

    results: list[dict] = []
    for gen_func in generators:
        facts, test_cases = gen_func()
        for i, tc in enumerate(test_cases):
            results.append({
                "id": f"synthetic-{tc.ability}-{i:03d}",
                "ability": tc.ability,
                "sessions": [
                    {
                        "session_id": "s1",
                        "messages": [
                            {"role": "user", "content": f["content"]}
                            for f in facts
                        ],
                    }
                ],
                "question": tc.query,
                "answer": ", ".join(tc.expected_keywords) if tc.expected_keywords else "ABSTAIN",
            })

    return results
